class ProductDetailView(DetailView):
    model = Product
    template_name = 'adminapp/product.html'


@user_passes_test(lambda u: u.is_superuser)
def product_update(request, pk):
    product_item = get_object_or_404(Product, pk=pk)
    if request.method == 'POST':
        product_form = ProductEditForm(request.POST, request.FILES, instance=product_item)
        if product_form.is_valid():
            product_form.save()
            return HttpResponseRedirect(reverse('admin:products', args=[product_item.category.pk]))
    else:
        product_form = ProductEditForm(instance=product_item)
    content = {
        'product': product_form,
        'product_category': {
            'pk': product_item.category.pk
        }
    }
    return render(request, 'adminapp/product_update.html', content)


@user_passes_test(lambda u: u.is_superuser)
def product_delete(request, pk):
    product_to_delete = get_object_or_404(Product, pk=pk)
    if request.method == 'POST':
        if product_to_delete.is_active:
            product_to_delete.is_active = False
        else:
            product_to_delete.is_active = True
        product_to_delete.save()
        return HttpResponseRedirect(reverse('admin:products', args=[product_to_delete.category.pk]))
    content = {
        'product_to_delete': product_to_delete,
        'product_category': {
            'pk': product_to_delete.category.pk
        }
    }
    return render(request, 'adminapp/product_delete.html', content)
